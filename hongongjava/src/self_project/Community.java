package self_project;

public class Community {
	private int comu_num;
	private String comu_title;
	private String comu_content;
	private String user_id;
	
	
	public int get_comu_num() {
		return comu_num;
	}
	public void set_comu_num(int comu_num) {
		this.comu_num = comu_num;
	}
	public String get_comu_title() {
		return comu_title;
	}
	public void set_comu_title(String comu_title) {
		this.comu_title = comu_title;
	}
	public String get_comu_content() {
		return comu_content;
	}
	public void set_comu_content(String comu_content) {
		this.comu_content = comu_content;
	}
	public String get_user_id() {
		return user_id;
	}
	public void set_user_id(String user_id) {
		this.user_id = user_id;
	}
}
