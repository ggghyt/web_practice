package self_project;

public class Comments {
	private int comm_num;
	private int comu_num;
	private String comm_content;
	private String user_id;
	
	public int get_comm_num() {
		return comm_num;
	}
	public void set_comm_num(int comm_num) {
		this.comm_num = comm_num;
	}
	public int get_comu_num() {
		return comu_num;
	}
	public void set_comu_num(int comu_num) {
		this.comu_num = comu_num;
	}
	public String get_comm_content() {
		return comm_content;
	}
	public void set_comm_content(String comm_content) {
		this.comm_content = comm_content;
	}
	public String get_user_id() {
		return user_id;
	}
	public void set_user_id(String user_id) {
		this.user_id = user_id;
	}
	
}
