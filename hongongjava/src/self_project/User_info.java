package self_project;

public class User_info {
	private String user_id;
	private String user_pw;
	private String user_name;
	private String user_phone;
	private String user_addr;
	private String user_position;
	
	public String get_user_id() {
		return user_id;
	}
	public void set_user_id(String user_id) {
		this.user_id = user_id;
	}
	public String get_user_pw() {
		return user_pw;
	}
	public void set_user_pw(String user_pw) {
		this.user_pw = user_pw;
	}
	public String get_user_name() {
		return user_name;
	}
	public void set_user_name(String user_name) {
		this.user_name = user_name;
	}
	public String get_user_phone() {
		return user_phone;
	}
	public void set_user_phone(String user_phone) {
		this.user_phone = user_phone;
	}
	public String get_user_addr() {
		return user_addr;
	}
	public void set_user_addr(String user_addr) {
		this.user_addr = user_addr;
	}
	public String get_user_position() {
		return user_position;
	}
	public void set_user_position(String user_position) {
		this.user_position = user_position;
	}
	
	
}
